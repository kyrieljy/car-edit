function workflowNode(
  id: string,
  type: WorkflowNodeConfig["type"],
  label: string,
  x: number,
  y: number,
  patch: Partial<WorkflowNodeConfig> = {},
): WorkflowNodeConfig {
  return {
    id,
    type,
    label,
    description: "",
    position: { x, y },
    required: true,
    enabled: true,
    providerCapability: "",
    providerId: "",
    fallbackProviderId: "",
    promptTemplateId: "",
    failureStrategy: "stop",
    maxRetries: 0,
    config: {},
    ...patch,
  }
}

function workflowEdges(ids: string[]): WorkflowEdgeConfig[] {
  return ids.slice(0, -1).map((id, index) => ({
    id: `edge_${id}_${ids[index + 1]}`,
    source: id,
    target: ids[index + 1],
  }))
}

