const refRoot = "/assets/parts/references/multi-ref-v1"

function refs(assetId: string, folder: string, files: string[], hint: string, role: PartAssetReference["role"] = "full_part_reference"): PartAssetReference[] {
  return files.map((file, index) => ({
    id: `${assetId}-ref-${index + 1}`,
    assetId,
    url: `${refRoot}/${folder}/${file}`,
    role,
    view: index === 0 ? "primary" : `reference-${index + 1}`,
    priority: index + 1,
    promptHint: hint,
    uploadToModel: true,
    active: true,
    createdAt: 0,
  }))
}

